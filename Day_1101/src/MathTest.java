
public class MathTest {

	public static void main(String[] args) {
		System.out.println(Math.ceil(56.1890)); // 올림
		System.out.println(Math.floor(56.7890)); // 버림
		System.out.println(Math.max(56, 55)); // 최대값
		System.out.println(Math.pow(2, 3)); // 거듭제곱
		System.out.println(Math.round(56.7890)); // 반올림
		System.out.println(Math.round(56.1890)); // 반올림
		System.out.println(Math.sqrt(4)); // 루트

	}

}
