
public class Third {
	public static void main(String[] args) {
		System.out.println(5 * 2 * 3.14);
		System.out.println(5*5 * 3.14);
		System.out.println(5*7/2);
		// +, -, * /
		// 반지름이 5인 원의 면적을 구하여 출력!
		// 원의 둘레 = 반지름 * 2 * 3.14
		// 원의 넓이 = 반지름 * 반지름 * 3.14
		// 삼각형의 밑변 5, 높이 7일 때 면적
		// 삼각형의 넓이= 밑변 * 높이 / 2
	}

}
