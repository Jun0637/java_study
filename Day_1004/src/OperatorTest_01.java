
public class OperatorTest_01 {

	public static void main(String[] args) {
		int result;
		result = 10 + 20;
		
		System.out.println(result);
		
		result = 10 - 20;// 변수 선언은 한번만
		System.out.println(result);
		
		result = 10 * 20;
		System.out.println(result);
		
		result = 10 / 20;
		System.out.println(result);// /는 정수와 정수로 나누면 결과는 정수
		
		result = 10 % 20;// 나머지
		System.out.println(result);
	}

}
